import fs from "node:fs";
import path from "node:path";
import {
  DEFAULT_PE_EXAM_MAX_AGE_DAYS,
  buildPeExamSourceStatus,
} from "./lib/peExamSourceStatus.mjs";

const outFile = path.resolve("app/pe-exam/peExamSourceStatus.js");
const status = buildPeExamSourceStatus({ skipAgeCheck: true });
const sourceGeneratedTimes = status.sources
  .map((source) => Date.parse(source.generatedAt))
  .filter((time) => Number.isFinite(time));
const sourceSnapshotsMaxGeneratedAt = sourceGeneratedTimes.length
  ? new Date(Math.max(...sourceGeneratedTimes)).toISOString()
  : "";

const snapshot = {
  statusVersion: 1,
  generatedBy: "scripts/write-pe-exam-source-status.mjs",
  sourceSnapshotsMaxGeneratedAt,
  defaults: {
    maxAgeDays: DEFAULT_PE_EXAM_MAX_AGE_DAYS,
  },
  sources: status.sources.map((source) => ({
    key: source.key,
    label: source.label,
    file: source.file,
    exportName: source.exportName,
    sourceName: source.sourceName,
    sourceUrl: source.sourceUrl,
    schoolYear: source.schoolYear,
    resultYear: source.resultYear,
    generatedAt: source.generatedAt,
    universityCount: source.universityCount,
    admissionCount: source.admissionCount,
    minSchoolYearOffset: source.minSchoolYearOffset,
    minResultYearOffset: source.minResultYearOffset,
    minUniversities: source.minUniversities,
    minAdmissions: source.minAdmissions,
  })),
};

const content = `// Generated by scripts/write-pe-exam-source-status.mjs.\n` +
  `// Do not edit manually; run npm.cmd run pe-exam:data:status after source snapshots change.\n` +
  `export const peExamSourceStatusSnapshot = ${JSON.stringify(snapshot, null, 2)};\n`;

fs.writeFileSync(outFile, content, "utf8");
console.log(`Wrote ${path.relative(process.cwd(), outFile)}`);
