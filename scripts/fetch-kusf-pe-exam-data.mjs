import fs from "node:fs/promises";
import path from "node:path";

const SOURCE_URL = "https://info.kusf.or.kr/kusf/portal/bbs/kusfeipetcselctninf/selectEntEtcSelctnList.do";
const OUT_FILE = path.resolve("app/pe-exam/kusfAdmissionData.ts");
const SCHOOL_YEAR = "2026";

function cleanText(value) {
  return String(value || "")
    .replace(/<br\s*\/?>/gi, " ")
    .replace(/&nbsp;/g, " ")
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/<[^>]+>/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function parseUniversityList(html) {
  const start = html.indexOf('id="univListUl"');
  const end = html.indexOf("</ul>", start);
  const listHtml = html.slice(start, end + 5);
  const itemRe = /id="lst_univ_cd(\d+)" value="(\d+)"[\s\S]*?<span class="type">([^<]+)<\/span>\s*<span>([^<]+)<\/span><span class="area">\/\s*([^<]+)<\/span>/g;

  return [...listHtml.matchAll(itemRe)].map((match) => {
    const rawName = match[4];
    const campus = rawName.match(/\[([^\]]+)\]/)?.[1] || "";

    return {
      code: match[2],
      area: match[5],
      schoolType: match[3],
      name: rawName.replace(/\[[^\]]+\]/g, ""),
      campus,
    };
  });
}

function parseAdmissionRows(html) {
  const blockRe = /<div class="result_box_list[^"]*">([\s\S]*?)(?=<div class="result_box_list|<\/div><!-- result_box_wrap -->)/g;
  const rowRe = /<tr>\s*<th[^>]*>([\s\S]*?)<\/th>\s*<td>([\s\S]*?)<\/td>\s*<\/tr>/g;

  return [...html.matchAll(blockRe)]
    .map((match) => {
      const block = match[1];
      const params = (block.match(/fn_slctyView_onclick\(([^)]*)\)/) || [])[1] || "";
      const paramValues = [...params.matchAll(/'([^']*)'/g)].map((item) => item[1]);
      const rowEntries = [...block.matchAll(rowRe)].map((row) => [cleanText(row[1]), cleanText(row[2])]);
      const rows = Object.fromEntries(rowEntries);
      const infoCell = (rowEntries.find(([label]) => label === "전형정보") || [])[1] || "";
      const paragraphTexts = [...(block.match(/<th[^>]*>\s*전형정보\s*<\/th>\s*<td>([\s\S]*?)<\/td>/) || [])[1]?.matchAll(/<p[^>]*>([\s\S]*?)<\/p>/g) || []]
        .map((paragraph) => cleanText(paragraph[1]))
        .filter(Boolean);
      const admissionName = paragraphTexts[0] || infoCell;
      const elementSummary = paragraphTexts.slice(1).join(" / ");

      return {
        unit: rows["모집단위"] || "",
        admissionName,
        elementSummary,
        admissionType: rows["전형유형"] || "",
        quota: rows["모집인원"] || "",
        detailParams: {
          schoolYear: paramValues[0] || SCHOOL_YEAR,
          universityCode: paramValues[1] || "",
          recruitmentCode: paramValues[2] || "20",
          selectionGroupCode: paramValues[3] || "",
          recruitmentUnitCode: paramValues[4] || "",
          recruitmentUnitSerial: paramValues[5] || "",
          schoolClassCode: paramValues[6] || "",
        },
      };
    })
    .filter((row) => row.unit || row.admissionName || row.admissionType || row.quota);
}

async function fetchAdmissions(university) {
  const body = new URLSearchParams({
    sch_year: SCHOOL_YEAR,
    univ_cd: university.code,
  });
  const response = await fetch(SOURCE_URL, {
    method: "POST",
    headers: {
      "content-type": "application/x-www-form-urlencoded",
    },
    body,
  });

  if (!response.ok) {
    throw new Error(`${university.name} ${response.status} ${response.statusText}`);
  }

  return parseAdmissionRows(await response.text());
}

async function mapWithConcurrency(items, limit, mapper) {
  const results = new Array(items.length);
  let index = 0;

  async function worker() {
    while (index < items.length) {
      const currentIndex = index;
      index += 1;
      results[currentIndex] = await mapper(items[currentIndex], currentIndex);
    }
  }

  await Promise.all(Array.from({ length: limit }, worker));
  return results;
}

function createOutput(data) {
  const payload = JSON.stringify(data, null, 2);

  return `// Generated by scripts/fetch-kusf-pe-exam-data.mjs.\n` +
    `// Source: KUSF 체육특기자대입포털 체육관련학과 대입전형, ${SCHOOL_YEAR}학년도 수시 일반전형.\n` +
    `// Do not edit by hand unless the official source is unavailable and the change is documented.\n\n` +
    `export const kusfAdmissionSnapshot = ${payload} as const;\n`;
}

async function main() {
  const mainHtml = await fetch(SOURCE_URL).then((response) => {
    if (!response.ok) throw new Error(`${response.status} ${response.statusText}`);
    return response.text();
  });
  const universities = parseUniversityList(mainHtml);

  const enriched = await mapWithConcurrency(universities, 5, async (university) => {
    const admissions = await fetchAdmissions(university);

    return {
      ...university,
      track: "수시",
      source: "KUSF 체육관련학과 대입전형",
      admissions,
    };
  });

  const data = {
    schoolYear: SCHOOL_YEAR,
    recruitmentTrack: "수시",
    sourceName: "KUSF 체육특기자대입포털 체육관련학과 대입전형",
    sourceUrl: SOURCE_URL,
    generatedAt: new Date().toISOString(),
    coverageNote:
      "KUSF 체육관련학과 대입전형 페이지에서 제공하는 2026학년도 수시 일반전형 요약입니다. 정시, 실기 기록 세부 기준, 등급·입결은 대학별 모집요강과 입결 자료로 별도 검수해야 합니다.",
    universities: enriched,
  };

  await fs.writeFile(OUT_FILE, createOutput(data), "utf8");

  const schoolsWithAdmissions = enriched.filter((item) => item.admissions.length > 0).length;
  const admissionCount = enriched.reduce((sum, item) => sum + item.admissions.length, 0);

  console.log(`Wrote ${OUT_FILE}`);
  console.log(`${universities.length} universities, ${schoolsWithAdmissions} with admissions, ${admissionCount} admission rows`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
